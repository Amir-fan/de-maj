// Language detection and storage
export class TranslationManager {
    constructor() {
        this.currentLanguage = this.getStoredLanguage() || this.detectLanguage();
        this.init();
    }
    
    getStoredLanguage() {
        return localStorage.getItem('de-maj-language');
    }
    
    detectLanguage() {
        const browserLang = navigator.language.split('-')[0];
        const supportedLangs = ['en', 'ar', 'tr'];
        return supportedLangs.includes(browserLang) ? browserLang : 'en';
    }
    
    init() {
        this.applyTranslations();
        this.bindLanguageSwitcher();
        this.updateLanguageSwitcher();
    }
    
    applyTranslations() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.getTranslation(key);
            if (translation) {
                element.textContent = translation;
            }
        });
        
        // Update HTML lang attribute
        document.documentElement.lang = this.currentLanguage;
        
        // Update text direction for Arabic
        if (this.currentLanguage === 'ar') {
            document.documentElement.dir = 'rtl';
        } else {
            document.documentElement.dir = 'ltr';
        }
    }
    
    getTranslation(key) {
        const keys = key.split('.');
        let translation = translations[this.currentLanguage];
        
        for (const k of keys) {
            translation = translation?.[k];
        }
        
        return translation || key;
    }
    
    setLanguage(lang) {
        this.currentLanguage = lang;
        localStorage.setItem('de-maj-language', lang);
        this.applyTranslations();
        this.updateLanguageSwitcher();
    }
    
    bindLanguageSwitcher() {
        // Bind events to existing language switcher buttons
        const languageButtons = document.querySelectorAll('.language-btn');
        languageButtons.forEach(button => {
            button.addEventListener('click', (e) => {
                e.preventDefault();
                const lang = button.getAttribute('data-lang');
                if (lang) {
                    this.setLanguage(lang);
                }
            });
        });
    }
    
    updateLanguageSwitcher() {
        const buttons = document.querySelectorAll('.language-btn');
        buttons.forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === this.currentLanguage) {
                btn.classList.add('active');
            }
        });
    }
    
    getCurrentLanguageName() {
        const names = {
            'en': 'English',
            'ar': 'العربية',
            'tr': 'Türkçe'
        };
        return names[this.currentLanguage];
    }
}

// Initialize translation manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.translationManager = new TranslationManager();
});
